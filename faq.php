<br>
<h2>Am I, as a patient, able to cancel an appointment after I select one?</h2>
<h3>Absolutely! Our product aims to help patients connect with doctors whenever it is most convenient for you. Should you
    choose to cancel an appointment, we will make sure the doctor recieves a notification about your cancellation so that
    that spot may be choosen by another patient if needed.</h3>
<br>
<h2>Am I, as a doctor, able to cancel an appointment after a patient schedules one?</h2>
<h3>Our system currently does not allow for doctors to cancel appointments once a patient has scheduled one.
    if you do need to cancel a previously scheduled appointment, it is the doctor's responsibility to notify the patient. However,
    we do highly encourage doctors to verify the appointment times and dates they list in order to avoid these situations entirely. </h3>
<br>
<h2>I can't remember my password! What should I do? </h2>
<h3>You can recover your password by clicking on the 'Forgot your password?' link at the bottom of the login page.
    If you find for any reason that you are unable to log into your account, please contact our support team at sealofhealth@gmail.com
    for further instructions.</h3>
<br>
<h2>I didn't recieve an appointment confirmation. Was my appointment still created?</h2>
<h3>If you did not recieve an appointment confirmation email after scheduling an appointment, you should not assume that the appointment
    was created. Check your 'Current Appointments' tab in your profile and if you do not see the appointment, try to reschedule the appointment.
    If you continue to have issues, please contact our support team at sealofhealth@gmail.com.</h3>

